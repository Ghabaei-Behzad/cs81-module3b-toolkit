//Function 1 
function double(n) {
  return n * 2;
}
//Function 2 
function square(n) {
  return n * n;
}
//Function 3
function isEven(n) {
  return n % 2 === 0;
}
//Function 4
function multiply(a, b) {
  return a * b;
}
//Function 5
function power(base, exponent) {
  return Math.pow(base, exponent);
}
